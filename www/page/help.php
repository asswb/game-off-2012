<h1>Help</h1>
<p>GitHub Tycoon is a persistent software repository management simulation game.</p>
<h2>Glossary</h2>
  <p>CBQ .. Code Base Quality</p>
  <p>COM .. Community</p>
<h2>FAQ</h2>
<h3>What do the different issues do?</h3>
  <b>Issues effect your CBQ and COM both positively and negatively.</b> Here's the lowdown;
  <ul>
    <li>
      <h4>Enhancements</h4>
      <p>Upon completion, these issues will give you +2% to CBQ and +COM.</p>
    </li>
    <li>
      <h4>Bugs</h4>
      <p>Upon discovery, these issues will give you -2% to CBQ.</p>
      <p>Upon completion, these issues will give you +2% to CBQ and +COM.</p>
    </li>
    <li>
      <h4>Pull Requests</h4>
      <p>Upon completion, these issues will give you -10% to CBQ, but a large +COM.</p>
    </li>
  </ul>
<h3>Why can't I get to 100% CBQ?</h3>
 <p>For every bug you have, -2% is subtracted off of your 100% maximum CBQ.</p>
<h3>My score is zero! How is score calculated?</h3>
  <p>Score = CBQ * COM</p>
